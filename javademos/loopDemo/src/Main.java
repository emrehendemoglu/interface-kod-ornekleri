public class Main {

    public static void main(String[] args) {
	  //For
        for (int i=1;i<10;i+=2) {
            System.out.println(i);
        }
        System.out.println("For Döngüsü bitti ");

        int i=1;
        //While
         while(i<10){
             System.out.println(i);
            i++;
         }
     //infinite
        System.out.println("While Döngüsü bitti ");

       //Do-While
        int j=1;
        do{
            System.out.println(i);
            j+=2;
        }while(i<10);
      System.out.println("Do-Whle Döngüsü Bitti");
    }
}
